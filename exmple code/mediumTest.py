#! /usr/bin/env python
from mininet.cli import CLI
from mininet.net import Mininet
from mininet.link import Link,TCLink,Intf

if '__main__' == __name__:
  net = Mininet(link=TCLink)
  h1 = net.addHost('h1')
  h2 = net.addHost('h2')
  h3 = net.addHost('h3')
  r1 = net.addHost('r1')
  r2 = net.addHost('r2')
  r3 = net.addHost('r3')
  r4 = net.addHost('r4')
  net.addLink(h1, r1)
  net.addLink(h2, r1)
  net.addLink(r1, r2)
  net.addLink(r1, r3)
  net.addLink(r2, r4)
  net.addLink(r3, r4) 
  net.addLink(r4, h3)
  net.build()

  h1.cmd("ifconfig h1-eth0 0")
  h1.cmd("ip addr add 192.168.0.10/24 brd + dev h1-eth0")
  h1.cmd("ip route add default via 192.168.0.1")

  h2.cmd("ifconfig h2-eth0 0")
  h2.cmd("ip addr add 192.168.1.10/24 brd + dev h2-eth0")
  h2.cmd("ip route add default via 192.168.1.1") 

  r1.cmd("ifconfig r1-eth0 0")
  r1.cmd("ifconfig r1-eth1 0")
  r1.cmd("ifconfig r1-eth2 0")
  r1.cmd("ifconfig r1-eth3 0") 
  r1.cmd("ip addr add 192.168.0.1/24 brd + dev r1-eth0") 
  r1.cmd("ip addr add 192.168.1.1/24 brd + dev r1-eth1")
  r1.cmd("ip addr add 10.0.0.1/24 brd + dev r1-eth2")
  r1.cmd("ip addr add 10.0.1.1/24 brd + dev r1-eth3")
  r1.cmd("ip route add default via 10.0.0.2 table 10")
  r1.cmd("ip route add 192.168.0.0/24 dev r1-eth0 table 10")
  r1.cmd("ip route add 192.168.1.0/24 via 192.168.1.1 table 10")
  r1.cmd("ip route add default via 10.0.1.2 table 20")
  r1.cmd("ip route add 192.168.1.0/24 dev r1-eth1 table 20") 
  r1.cmd("ip route add 192.168.0.0/24 via 192.168.0.1 table 20") 
  r1.cmd("ip rule add pref 10 from 192.168.0.0/24 table 10")
  r1.cmd("ip rule add pref 20 from 192.168.1.0/24 table 20")
  r1.cmd("iptables -t nat -A POSTROUTING -o r1-eth2 -j MASQUERADE")
  r1.cmd("iptables -t nat -A POSTROUTING -o r1-eth3 -j MASQUERADE")

  r2.cmd("ifconfig r2-eth0 0")
  r2.cmd("ifconfig r2-eth1 0") 
  r2.cmd("ip addr add 10.0.0.254/24 brd + dev r2-eth0") 
  r2.cmd("ip addr add 10.0.2.254/24 brd + dev r2-eth1")
  r2.cmd("ip route add default via 10.0.2.2")

  r3.cmd("ifconfig r3-eth0 0")
  r3.cmd("ifconfig r3-eth1 0") 
  r3.cmd("ip addr add 10.0.1.2/24 brd + dev r3-eth0") 
  r3.cmd("ip addr add 10.0.3.1/24 brd + dev r3-eth1")
  r3.cmd("ip route add default via 10.0.3.2")
  
  r4.cmd("ifconfig r4-eth0 0")
  r4.cmd("ifconfig r4-eth1 0")  
  r4.cmd("ifconfig r4-eth2 0")
  r4.cmd("ip addr add 10.0.2.2/24 brd + dev r4-eth0") 
  r4.cmd("ip addr add 10.0.3.2/24 brd + dev r4-eth1")
  r4.cmd("ip addr add 192.168.100.1/24 brd + dev r4-eth2")
  r4.cmd("ip route add 10.0.0.0/24 via 10.0.2.1")
  r4.cmd("ip route add 10.0.1.0/24 via 10.0.3.1")

  h3.cmd("ifconfig h3-eth0 0")
  h3.cmd("ip addr add 192.168.100.10/24 brd + dev h3-eth0")
  h3.cmd("ip route add default via 192.168.100.1") 
    
  CLI(net)
  net.stop() 
